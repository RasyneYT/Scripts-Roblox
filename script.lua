### 3. Les Émojis Spéciaux (ID)
Si tu veux utiliser des émojis de ton serveur dans tes messages de groupe, utilise le format ID.
* **Comment faire :** Tape `\:nom_de_ton_emoji:` dans un salon, Discord va te donner un code comme `<:nom:123456789>`. Copie ce code dans ton message de groupe, et l'émoji s'affichera même si tu n'es pas sur le serveur !

